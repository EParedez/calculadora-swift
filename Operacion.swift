public class Operacion{


 public init() {

 }
 
  public func sumar(a:Int, b:Int) -> Int {
    return a+b
  }

  public func restar(a:Int, b:Int) -> Int {
    return a - b
  }

  public func dividir(a:Int, b:Int) -> Int {
    return a / b
  }

  public func multiplicar(a:Int, b:Int) -> Int {
    return a * b
  }

}